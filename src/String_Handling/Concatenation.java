package String_Handling;

public class Concatenation {
    public static void main(String[] args) {
        String a = "Tanir" ;
        String b = "is" ;
        String c = "good" ;
        String d = "boy" ;
        String comb = a + " " + b + " " + c + " " + d ;
        System.out.println("The final message is: " + comb);
    }
}
